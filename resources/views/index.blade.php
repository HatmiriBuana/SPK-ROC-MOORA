        <div class="section-title">
          <h2>Studi Kasus</h2>
          <p>Pemberian beasiswa merupakan salah satu apresiasi dari lembaga kepada siswa didik yang kurang mampu untuk memotivasi peserta didik (siswa) lebih semangat dalam menempuh 
            pendidikan. Pemberian beasiswa yang tepat mampu memotivasi peserta didik pada suatu lembaga pendidikan, sebaliknya bila beasiswa yang diberikan salah sasaran maka yang 
            terjadi adalah peningkatan prestasi yang diharapkan tidak akan terlihat dari peserta didik. Agar pemberian beasiswa tepat maka penyeleksian dalam penerimaan beasiswa harus 
            lebih baik. Saat ini keputusan yang dikeluarkan terhadap penerima beasiswa oleh lembaga pendidikan masih dinilai kurang tepat, karena masih bersifat subjektif, didasarkan 
            faktor kekeluargaan, hubungan kedekatan dan lainnya, bukan dikarenakan kurang mampu, prestasi yang pernah di capai ataupun keaktifan dalam berorganisasi di lembaga pendidikan, 
            sehingga ketercapaian dari manfaat pemberian beasiswa kurang dirasakan. Untuk itu diperlukan suatu sistem yang memberikan bantuan dukungan terhadap keputusan yang 
            dihasilkan oleh pihak lembaga pendidikan. Sistem ini dikenal dengan nama Sistem Pendukung Keputusan (SPK). Pada seleksi bantuan siswa ini kami memilih kombinasi metode ROC
            untuk mendapatkan nilai bobot dan metode MOORA untuk mencari  perankingan.</p>
